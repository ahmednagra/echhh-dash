import React from 'react';

// Contact types (for ContactColumn)
export const CONTACT_TYPES = ['phone', 'email', 'whatsapp', 'telegram'];

// Social types (for SocialColumn)
export const SOCIAL_TYPES = [
  'tiktok',
  'youtube',
  'threads',
  'linkedin',
  'instagram',
  'twitter',
  'facebook',
  'snapchat',
];

// Check if a type is a contact type
export const isContactType = (type: string): boolean => {
  return CONTACT_TYPES.includes(type?.toLowerCase());
};

// Check if a type is a social type
export const isSocialType = (type: string): boolean => {
  return SOCIAL_TYPES.includes(type?.toLowerCase());
};

// NEW: Get effective social type from contact (checks both contact_type and name)
export const getEffectiveSocialType = (contact: any): string | null => {
  const contactType = (
    contact.contact_type ||
    contact.type ||
    ''
  ).toLowerCase();
  const name = (contact.name || '').toLowerCase();

  // If contact_type is a valid social type, use it
  if (isSocialType(contactType)) {
    return contactType;
  }

  // If contact_type is 'other' but name is a valid social type, use name
  if (contactType === 'other' && isSocialType(name)) {
    return name;
  }

  // Check name field regardless of contact_type
  if (isSocialType(name)) {
    return name;
  }

  return null;
};

